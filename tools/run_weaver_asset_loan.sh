start_networks() {
    log_section "Starting Blockchain Networks"
    cd "${WEAVER_ROOT}" || { log_error "Failed to cd to ${WEAVER_ROOT}"; exit 1; }

    # Fabric first to give the chaincode build (memory-heavy) headroom before
    # Corda's two JVM-heavy networks consume the bulk of host RAM.
    log_info "Starting Fabric Network with ${CHAINCODE_NAME} chaincode..."
    cd tests/network-setups/fabric/dev || { log_error "Failed to cd to fabric/dev"; exit 1; }
    if ! make start-interop-local CHAINCODE_NAME=${CHAINCODE_NAME}; then
        log_error "Failed to start Fabric Network"; exit 1
    fi
    cd ../../../.. || { log_error "Failed to cd back"; exit 1; }

    log_info "Starting Corda Network (a few minutes)..."
    cd tests/network-setups/corda || { log_error "Failed to cd to corda"; exit 1; }
    if ! make start-local; then
        log_error "Failed to start Corda Network"; exit 1
    fi
    cd ../../.. || { log_error "Failed to cd back"; exit 1; }

    log_info "Recent Corda container logs..."
    docker logs --tail 20 corda_partya_1 || true
    docker logs --tail 20 corda_network2_partya_1 || true

    log_success "Networks started successfully"
}

start_relays() {
    log_section "Starting Relays in Docker"
    cd "${WEAVER_ROOT}/core/relay" || { log_error "Failed to cd to core/relay"; exit 1; }
    if ! make convert-compose-method2; then log_error "convert-compose-method2 failed"; exit 1; fi

    for net in n1 n2 corda corda2; do

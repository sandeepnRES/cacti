        sed -i.bak "s#^DOCKER_IMAGE_NAME=.*#DOCKER_IMAGE_NAME=cacti-weaver-relay-server#g" "docker/testnet-envs/.env.${net}"
        make start-server COMPOSE_ARG="--env-file docker/testnet-envs/.env.${net}"
        sleep 5
    done
    log_success "All relays started"
}

start_drivers() {
    log_section "Starting Drivers in Docker"
    cd "${WEAVER_ROOT}" || { log_error "Failed to cd to ${WEAVER_ROOT}"; exit 1; }

    log_info "Setting up Fabric Drivers..."
    cd core/drivers/fabric-driver || { log_error "Failed to cd"; exit 1; }
    for n in n1 n2; do
        sed -i.bak "s#^DOCKER_IMAGE_NAME=.*#DOCKER_IMAGE_NAME=cacti-weaver-driver-fabric#g" "docker-testnet-envs/.env.${n}"
        sed -i.bak "s#<PATH-TO-WEAVER>#${WEAVER_ROOT}#g" "docker-testnet-envs/.env.${n}"
    done
    for n in n1 n2; do
        log_info "Starting Fabric Driver for network${n#n}..."
        make deploy COMPOSE_ARG="--env-file docker-testnet-envs/.env.${n}" \
            NETWORK_NAME=$(grep NETWORK_NAME "docker-testnet-envs/.env.${n}" | cut -d '=' -f 2)
        sleep 5
    done
    cd ../../.. || { log_error "Failed to cd back"; exit 1; }

    log_info "Setting up Corda Drivers..."
    cd core/drivers/corda-driver || { log_error "Failed to cd"; exit 1; }
    for c in corda corda2; do
        sed -i.bak "s#^DOCKER_IMAGE_NAME=.*#DOCKER_IMAGE_NAME=cacti-weaver-driver-corda#g" "docker-testnet-envs/.env.${c}"
    done
    log_info "Starting Corda_Network Driver..."
    make deploy COMPOSE_ARG='--env-file docker-testnet-envs/.env.corda'
    sleep 5
    log_info "Starting Corda_Network2 Driver..."
    make deploy COMPOSE_ARG='--env-file docker-testnet-envs/.env.corda2'
    sleep 5
    cd ../../.. || { log_error "Failed to cd back"; exit 1; }

    log_success "All drivers started"
}

start_iin_agents() {
    log_section "Starting IIN Agents in Docker"
    cd "${WEAVER_ROOT}/core/identity-management/iin-agent" || { log_error "Failed to cd"; exit 1; }

    for env_file in docker-testnet/envs/.env.n1.org1 docker-testnet/envs/.env.n2.org1; do
        sed -i.bak "s#^DOCKER_IMAGE_NAME=.*#DOCKER_IMAGE_NAME=cacti-weaver-iin-agent#g" "$env_file"
        sed -i.bak "s#<PATH-TO-WEAVER>#${WEAVER_ROOT}#g" "$env_file"
        sed -i.bak "s#^AUTO_SYNC=true#AUTO_SYNC=false#g" "$env_file"
    done

    # Configs are mode 640 by default; iinagent user inside the container can't
    # read them as bind-mounts. Make them world-readable.
    chmod a+rX docker-testnet/configs/ 2>/dev/null || true
    chmod a+r docker-testnet/configs/*.json 2>/dev/null || true

    # Same fix for the peer-org tree bind-mounted at /opt/iinagent/extra (e.g.
    # /opt/iinagent/extra/connection-org1.docker.json). The deploy target only
    # chmods the top-level dir; files inside stay 640 and are unreadable by
    # the iinagent uid in the container.
    for org in network1/peerOrganizations/org1.network1.com \
               network2/peerOrganizations/org1.network2.com; do
        local dlt_dir="${WEAVER_ROOT}/tests/network-setups/fabric/shared/${org}"
        [ -d "$dlt_dir" ] && chmod -R a+rX "$dlt_dir" 2>/dev/null || true
    done

    log_info "Starting Fabric IIN Agent for network1..."
    make deploy COMPOSE_ARG='--env-file docker-testnet/envs/.env.n1.org1' \
        DLT_SPECIFIC_DIR=$(grep DLT_SPECIFIC_DIR docker-testnet/envs/.env.n1.org1 | cut -d '=' -f 2)
    sleep 5

    log_info "Starting Fabric IIN Agent for network2..."
    make deploy COMPOSE_ARG='--env-file docker-testnet/envs/.env.n2.org1' \
        DLT_SPECIFIC_DIR=$(grep DLT_SPECIFIC_DIR docker-testnet/envs/.env.n2.org1 | cut -d '=' -f 2)
    sleep 5

    log_success "IIN Agents started"
}


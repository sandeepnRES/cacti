###############################################################################
# Weaver Asset LOAN Docker-based Test Automation Script
#
# Mirrors run_weaver_at.sh but installs the simpleasset_loan chaincode and
# exercises asset-loan flows (HTLC + claim-and-pledge + repayment) across
# Fabric ↔ Fabric, Fabric ↔ Corda, Corda ↔ Fabric, and Corda ↔ Corda.
#
# Convention used here: "asset network" = where the bond/asset lives;
# "token network" = where the repayment tokens live.
#
# Usage: ./tools/run_weaver_asset_loan.sh [options]
# Options:
#   --skip-build       Skip building components and Docker images
#   --skip-tests       Skip running tests (only setup)
#   --cleanup          Stop networks/containers and exit
#   --clean-build      Run build cleanup (make clean / clean-local) and exit
#   --help             Show this help message
###############################################################################

set -e

# Force umask 022 for this script's lifetime so files generated by host-side
# Make targets (cp, sed, cryptogen, ccp-generate.sh, peer CLI, etc.) come out
# world-readable. Without this, an inherited umask of 0027 produces mode-640
# files that downstream Docker containers (run as non-root uids) cannot read,
# manifesting as EACCES on dnsconfig.json, connection-org1.docker.json, etc.
# Scoped to this script — does not change the user's interactive shell umask.
umask 022

shopt -s expand_aliases 2>/dev/null || true

if [ -f ~/.bashrc ]; then
    source ~/.bashrc 2>/dev/null || true
elif [ -f ~/.zshrc ]; then
    source ~/.zshrc 2>/dev/null || true
fi

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
WEAVER_ROOT="${REPO_ROOT}/weaver"

CHAINCODE_NAME="simpleasset_loan"

SKIP_BUILD=false
SKIP_TESTS=false
CLEANUP=false
CLEAN_BUILD_ONLY=false

while [[ $# -gt 0 ]]; do
    case $1 in
        --skip-build)   SKIP_BUILD=true; shift;;
        --skip-tests)   SKIP_TESTS=true; shift;;
        --cleanup)      CLEANUP=true; shift;;
        --clean-build)  CLEAN_BUILD_ONLY=true; shift;;
        --help)         grep "^#" "$0" | grep -v "#!/bin/bash" | sed 's/^# //'; exit 0;;
        *) echo -e "${RED}Unknown option: $1${NC}"; exit 1;;
    esac
done

log_info()    { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_warning() { echo -e "${YELLOW}[WARNING]${NC} $1"; }
log_error()   { echo -e "${RED}[ERROR]${NC} $1"; }
log_section() {
    echo ""
    echo -e "${GREEN}========================================${NC}"
    echo -e "${GREEN}$1${NC}"
    echo -e "${GREEN}========================================${NC}"
}

# Network teardown — same as run_weaver_at.sh.
cleanup_networks() {
    log_section "Cleaning up networks and Docker containers"
    cd "${WEAVER_ROOT}" || { log_error "Failed to cd to ${WEAVER_ROOT}"; return 1; }

    _stop_in() {
        local subdir="$1"; shift
        if [ ! -d "${WEAVER_ROOT}/${subdir}" ]; then
            log_warning "Directory not found: ${subdir} — skipping"
            return 0
        fi
        (
            cd "${WEAVER_ROOT}/${subdir}" || { log_error "cd failed: ${subdir}"; exit 1; }
            for cmd in "$@"; do
                eval "$cmd" || log_warning "[$subdir] '$cmd' failed (continuing)"
            done
        ) || log_warning "Cleanup step in ${subdir} reported errors"
    }

    log_info "Stopping Corda drivers..."
    _stop_in "core/drivers/corda-driver" \
        "make stop COMPOSE_ARG='--env-file docker-testnet-envs/.env.corda'" \
        "make stop COMPOSE_ARG='--env-file docker-testnet-envs/.env.corda2'"

    log_info "Stopping Fabric drivers..."
    _stop_in "core/drivers/fabric-driver" \
        "make stop COMPOSE_ARG='--env-file docker-testnet-envs/.env.n1'" \
        "make stop COMPOSE_ARG='--env-file docker-testnet-envs/.env.n2'"

    log_info "Stopping IIN Agents..."
    _stop_in "core/identity-management/iin-agent" \
        "make stop COMPOSE_ARG='--env-file docker-testnet/envs/.env.n1.org1'" \
        "make stop COMPOSE_ARG='--env-file docker-testnet/envs/.env.n2.org1'"

    log_info "Stopping Relays..."
    _stop_in "core/relay" \
        "make convert-compose-method2" \
        "make stop COMPOSE_ARG='--env-file docker/testnet-envs/.env.n1'" \
        "make stop COMPOSE_ARG='--env-file docker/testnet-envs/.env.n2'" \
        "make stop COMPOSE_ARG='--env-file docker/testnet-envs/.env.corda'" \
        "make stop COMPOSE_ARG='--env-file docker/testnet-envs/.env.corda2'" \
        "make convert-compose-method1"

    log_info "Stopping Fabric networks..."
    _stop_in "tests/network-setups/fabric/dev" "make clean"

    log_info "Stopping Corda networks..."
    _stop_in "tests/network-setups/corda" "make clean"

    log_success "Cleanup completed"
}

cleanup_build() {
    log_section "Cleaning Weaver Build Artifacts"
    local dirs=(
        "common/protos-go"
        "common/protos-js"
        "common/protos-java-kt"
        "core/network/corda-interop-app"
        "sdks/corda"
        "samples/corda/corda-simple-application"
        "sdks/fabric/interoperation-node-sdk"
        "samples/fabric/fabric-cli"
        "core/relay"
        "core/drivers/fabric-driver"
        "core/drivers/corda-driver"
        "core/identity-management/iin-agent"
    )
    for dir in "${dirs[@]}"; do
        local full="${WEAVER_ROOT}/${dir}"
        if [ ! -f "${full}/Makefile" ] && [ ! -f "${full}/makefile" ] && [ ! -f "${full}/MAKEFILE" ] && [ ! -f "${full}/GNUmakefile" ]; then
            log_warning "No Makefile in ${dir}, skipping"; continue
        fi
        if (cd "${full}" && make -n clean-local) &> /dev/null; then
            log_info "Cleaning ${dir} (clean-local)..."
            (cd "${full}" && make clean-local) || log_warning "make clean-local failed in ${dir}"
        elif (cd "${full}" && make -n clean) &> /dev/null; then
            log_info "Cleaning ${dir} (clean)..."
            (cd "${full}" && make clean) || log_warning "make clean failed in ${dir}"
        else
            log_warning "No clean or clean-local target in ${dir}, skipping"
        fi
    done
    log_success "Build cleanup completed"
}

check_prerequisites() {
    log_section "Checking Prerequisites"
    local missing_deps=()
    command -v docker  &> /dev/null || missing_deps+=("Docker")
    command -v java    &> /dev/null || missing_deps+=("Java 17")
    command -v go      &> /dev/null || missing_deps+=("Go 1.20.2+")
    command -v node    &> /dev/null || missing_deps+=("Node.js v18.18.2+")
    command -v make    &> /dev/null || missing_deps+=("Make")
    if [ ${#missing_deps[@]} -ne 0 ]; then
        log_error "Missing dependencies: ${missing_deps[*]}"; exit 1
    fi
    log_success "All prerequisites satisfied"
}

setup_protoc() {
    log_section "Setting up Protoc"
    if command -v protoc &> /dev/null; then
        local v=$(protoc --version | awk '{print $2}')
        log_info "Found protoc version: $v"
        if [ "$(printf '%s\n' "3.12" "$v" | sort -V | head -n1)" = "3.12" ]; then
            log_success "Protoc already installed with sufficient version"
            if ! command -v protoc-gen-go &> /dev/null || ! command -v protoc-gen-go-grpc &> /dev/null; then
                go install google.golang.org/protobuf/cmd/protoc-gen-go@v1.34.2
                go install google.golang.org/grpc/cmd/protoc-gen-go-grpc@v1.4.0
            fi
            return 0
        fi
    fi
    cd "${REPO_ROOT}"
    if [ ! -d "protoc" ]; then
        curl -LO https://github.com/protocolbuffers/protobuf/releases/download/v3.15.6/protoc-3.15.6-linux-x86_64.zip
        unzip -q protoc-3.15.6-linux-x86_64.zip -d protoc
        rm protoc-3.15.6-linux-x86_64.zip
    fi
    export PATH="${PATH}:${REPO_ROOT}/protoc/bin"
    go install google.golang.org/protobuf/cmd/protoc-gen-go@v1.34.2
    go install google.golang.org/grpc/cmd/protoc-gen-go-grpc@v1.4.0
    log_success "Protoc setup completed"
}

